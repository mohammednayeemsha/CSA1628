# Create vectors
numeric_data <- c(1, 2, 3, 4, 5)
character_data <- c("apple", "banana", "cherry")
logical_data <- c(TRUE, FALSE, TRUE)

# Display the vectors and their types
cat("Numeric Data: ", numeric_data, " Type: ", typeof(numeric_data), "\n")
cat("Character Data: ", character_data, " Type: ", typeof(character_data), "\n")
cat("Logical Data: ", logical_data, " Type: ", typeof(logical_data), "\n")
